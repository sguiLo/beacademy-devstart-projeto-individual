<div class="fs-5 mt-3 container w-75">
    <p>O Clube Atlético Mineiro é um time de futebol criado em 25 de março de 1908, em Belo Horizonte, por um grupo de estudantes reunidos em um coreto no centro da cidade, que logo ficou conhecido como o “time do povo”, levando a uma grande identificação com o público. Seus fundadores foram: Aleixanor Alves Pereira, Antônio Antunes Filho, Augusto Soares, Benjamim Moss Filho, Carlos Maciel, Eurico Catão, Francisco Monteiro, Hugo Fracarolli, Humberto Moreira, Horácio Machado, João Barbosa Sobrinho, Jorge Dias Pena, José Soares Alves, Júlio Menezes Mello, Leônidas Fulgêncio, Margival Mendes Leal, Mário Neves, Mário Lott, Mário Toledo, Mauro Brochado, Raul Fracarolli e Sinval Moreira.</p>

    <p>Seu início já foi marcado pela vitória: logo na sua primeira disputa oficial, ocorrida em 1909, contra o Sport Club Futebol, o Atlético venceu. Após duas revanches, o Galo continuou vencendo, colocando um ponto final na existência do Sport Club. Além disso, venceu o primeiro campeonato estadual oficial em 1915. Muitos anos mais tarde, em 1971, venceu a primeira edição do Campeonato Brasileiro e em 1992, a primeira edição da Copa Conmebol.</p>

    <p>A história do Atlético Mineiro também encontra a inovação, uma vez ter sido o primeiro clube mineiro a adotar as bolas de couro em detrimento das bolas de meia. Foi também o primeiro a disputar uma partida contra um time estrangeiro, o Victória de Setúbal, de Portugal, batendo o adversário por 3x1. O primeiro jogador de futebol de campo a ser convidado a integrar a seleção brasileira, vindo de times que não pertenciam aos estados de São Paulo e Rio de Janeiro, foi do Atlético: Mário de Castro. Tão atleticano era o jogador, que dispensou o convite de jogar na seleção.</p>

    <p>Torcidas organizadas também merecem destaque, uma vez que elas sempre acompanham o time onde quer que ele jogue. A primeira sugestão de uma organização na torcida atleticana se deu em função da mãe de Mário Neves, um dos fundadores do time. Dona Alice Neves costurava bandeirinhas para que as moças da época pudessem torcer para o Atlético, e foi com essas vozes femininas que o hino do Atlético foi cantado pela primeira vez no estádio. No entanto, a torcida mais antiga do Galo, que até hoje marca presença nos jogos é a Dragões da FAO (Força Atleticana de Ocupação). Além dessa torcida, o galo conta com mais trinta e duas.</p>
